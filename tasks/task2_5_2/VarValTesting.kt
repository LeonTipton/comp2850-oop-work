fun main() {
    val myAge = 25u
    val universeAge = 13_800_000_000L
    val status = 'M'
    val name = "Sarah"
    val height = 1.78f 
    val root2 = Math.sqrt(2.0)

    println("myAge::class = ${myAge::class}")
    println("universeAge::class = ${universeAge::class}")
    println("status::class = ${status::class}")
    println("name::class = ${name::class}")
    println("height::class = ${height::class}")
    println("root2::class = ${root2::class}")
}